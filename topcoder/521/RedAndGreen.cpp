class RedAndGreen {
public:
	int minPaints(string);
};

int RedAndGreen::minPaints(string row) {

	int len = row.length(), mini = INT_MAX, i, j, cnt;

	for(i=0; i<len; i++)
	{
		cnt = 0;
		for(j=0; j<i; j++)
			if(row[j] == 'G')
				++cnt;

		for(;j<len;j++)
			if(row[j] == 'R')
				++cnt;

		if(cnt < mini)
			mini = cnt;
	}

	return mini;
}


double test0() {
	string p0 = "RGRGR";
	RedAndGreen * obj = new RedAndGreen();
	clock_t start = clock();
	int my_answer = obj->minPaints(p0);
	clock_t end = clock();
	delete obj;
	cout <<"Time: " <<(double)(end-start)/CLOCKS_PER_SEC <<" seconds" <<endl;
	int p1 = 2;
	cout <<"Desired answer: " <<endl;
	cout <<"\t" << p1 <<endl;
	cout <<"Your answer: " <<endl;
	cout <<"\t" << my_answer <<endl;
	if (p1 != my_answer) {
		cout <<"DOESN'T MATCH!!!!" <<endl <<endl;
		return -1;
	}
	else {
		cout <<"Match :-)" <<endl <<endl;
		return (double)(end-start)/CLOCKS_PER_SEC;
	}
}
