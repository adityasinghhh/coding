class Pillars {
public:
	double getExpectedLength(int, int, int);
};

double Pillars::getExpectedLength(int w, int x, int y) {

	if(x > y) { swap(x, y); }

	double ediff = x * sqrt(1.0*w*w);
	int j = y-1, cnt = 1;
	while(cnt < x)
	{
		if(j == x)
		{
			break;
		}

		ediff += cnt * sqrt(1.0*j*j + 1.0*w*w);
		++cnt;
		--j;
	}

	if(j == x)
	{
		int diff = y-x;
		while(diff < j)
		{
			ediff += cnt * sqrt(1.0*j*j + 1.0*w*w);
			--j;
			cnt += 2;
		}
		
		while(j > 0)
		{
			ediff += cnt * sqrt(1.0*j*j + 1.0*w*w);
			++cnt;
			--j;
		}
	}
	else
	{
		while(x < j)
		{
			ediff += cnt * sqrt(1.0*j*j + 1.0*w*w);
			--j;
		}

		while(j > 0)
		{
			ediff += cnt * sqrt(1.0*j*j + 1.0*w*w);
			++cnt;
			--j;
		}
	}

	return ediff/x/y;
}


