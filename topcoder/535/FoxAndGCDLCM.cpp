class FoxAndGCDLCM {
public:
	long long get(long long, long long);
};

long long FoxAndGCDLCM::get(long long G, long long L) {
	
	if(G > L) { return -1ll; }

	if(L % G != 0) { return -1ll; }

	LL x = L/G, mini = LLONG_MAX;
	for(LL i = 1; i*i <= x; ++i)
	{
		if(x%i == 0)
		{
			if(__gcd(i, x/i) == 1)
			{
				mini = MIN(mini, G*i + G*(x/i));
			}
		}
	}

	if(mini == LLONG_MAX) { return -1ll; }

	return mini;
}


