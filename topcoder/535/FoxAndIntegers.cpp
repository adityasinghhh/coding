class FoxAndIntegers {
public:
	vector <int> get(int, int, int, int);
};

vector <int> FoxAndIntegers::get(int AminusB, int BminusC, int AplusB, int BplusC) {

	int a, b, c;

	int a2 = AminusB + AplusB;

	if(a2&1)
	{
		VI v; return v;
	}

	a = a2 / 2;

	int b2 = BminusC + BplusC;

	if(b2&1)
	{
		VI v; return v;
	}

	b = b2 / 2;

	c = BplusC - b;

	int ba = AplusB - a;

	if(b != ba)
	{
		VI v; return v;
	}

	VI v;
	v.PB(a);
	v.PB(b);
	v.PB(c);

	return v;
	
}


