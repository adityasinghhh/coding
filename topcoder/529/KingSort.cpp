vector <string> KingSort::getSortedList(vector <string> kings) {
	init();

	vector<pair<string, pair<int, string> > > v;
	int sz = kings.size();
	REP(i,sz)
	{
		istringstream s(kings[i]);

		string s1, s2;
		s >> s1; s >> s2;

		v.PB(MP(s1, MP(m[s2], s2)));
	}

	SORT(v);

	vector<string> temp;
	REP(i,sz)
	{
		temp.PB(v[i].first + " " + v[i].second.second);
	}

	return temp;
}


