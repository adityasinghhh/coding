class UnsortedSequence {
public:
	vector <int> getUnsorted(vector <int>);
};

vector <int> UnsortedSequence::getUnsorted(vector <int> s) {
	SORT(s);
	int sz = s.size();
	int maxi = s[sz-1], j = sz-1;
	
	while(j >=0 && s[j] == maxi) { --j; }

	if(j == -1) { VI v; return v; }

	swap(s[j], s[j+1]);

	return s;	
}


