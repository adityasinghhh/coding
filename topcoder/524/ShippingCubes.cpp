class ShippingCubes {
public:
	int minimalCost(int);
};

int ShippingCubes::minimalCost(int N) {
	int i, j, k, mini = INT_MAX;
	for(i=1; i<=200; ++i)
	for(j=1; j<=200; ++j)
	for(k=1; k<=200; ++k)
	{
		if(i*j*k != N)
			continue;
		if(i+j+k < mini)
			mini = i+j+k;
	}

	return mini;
	
}


