class BlockTower {
public:
	int getTallest(vector <int>);
};

int dp[110][2];
VI v;

int f(int i, int flag)
{
	if(i == v.size())
	{
		return 0;
	}

	if(dp[i][flag] != -1)
	{
		return dp[i][flag];
	}

	if(flag)
	{
		if(v[i]&1)
		{
			return dp[i][flag] = v[i] + f(i+1, flag);
		}
		else
		{
			return dp[i][flag] = f(i+1, flag);
		}
	}
	else
	{
		if(v[i]&1)
		{
			return dp[i][flag] = MAX(v[i] + f(i+1, 1), f(i+1, flag));
		}
		else
		{
			return dp[i][flag] = v[i] + f(i+1, flag);
		}
	}
}

int BlockTower::getTallest(vector <int> v) {
	FILL(dp, -1);
	::v = v;
	int sz = v.size();

	return f(0, 0);
}


